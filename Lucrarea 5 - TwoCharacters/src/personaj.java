public class personaj
{
    private int num;
    private String culoare;
    private int nr_ochi;
    private int nr_vieti;

    public void setNum(int numar)
    {
        num=numar;
    }
    public int getNum()
    {
        return num;
    }
    public void setCuloare(String cul)
    {
        culoare=cul;
    }

    public String getCuloare()
    {
        return culoare;
    }

    public void setNr_ochi(int ochi)
    {
        nr_ochi=ochi;
    }

    public int getNr_ochi()
    {
        return nr_ochi;
    }

    public void setNr_vieti(int vieti)
    {
        nr_vieti=vieti;
    }

    public int getNr_vieti()
    {
        return nr_vieti;
    }
}
